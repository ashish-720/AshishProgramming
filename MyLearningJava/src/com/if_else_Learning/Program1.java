package com.if_else_Learning;

public class Program1 {
    public static void main(String[] args) {
        //even odd program
        int number = 13;
        if (number % 2 == 0) {
            System.out.println("Number " + number + " is EVEN");
        } else
            System.out.println("Number " + number + " is ODD");
    }
}
